public class demo04Array {
    public static void main(String[] args) {
        int[] array = { 15,25,30,40,50 };
        //遍历输入for循环5.fori 快捷键
        //array.fori 快捷方式
        for (int i = 0; i < array.length; i++) {
            System.out.println(array[i]);
        }

    }
}
